    public static void openUrl(Activity activity, String url) {
        if (StringUtils.isNullOrEmpty(url)) return;
        Uri uri = Uri.parse(url);
        if (uri.getScheme().equals("outhttp")) {
            Uri uriForOpen = Uri.parse(url.substring(3));
            Intent intent = new Intent(Intent.ACTION_VIEW, uriForOpen);
            activity.startActivity(intent);
        } else {
            Intent intent = new Intent(Intent.ACTION_VIEW, uri);
            activity.startActivity(intent);
        }
    }


    public static boolean isAssetFileExists(String pathInAssetsDir) {
        if (StringUtils.isNullOrEmpty(pathInAssetsDir)) return false;
        AssetManager assetManager = WXEnvironment.getApplication().getResources().getAssets();
        InputStream inputStream = null;
        try {
            inputStream = assetManager.open(pathInAssetsDir);
            if (null != inputStream) {
                return true;
            }
        } catch (IOException e) {
        } finally {
            try {
                if (null != inputStream) inputStream.close();
            } catch (IOException e) {
            }
        }
        return false;
    }

