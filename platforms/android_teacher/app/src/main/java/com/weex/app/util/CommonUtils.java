    public static String getLanguage(Context context) {
        String language = getSharedPreferencesString(context, "APP_LANGUAGE");
        if (StringUtils.isNullOrEmpty(language)) {
            Locale locale = context.getResources().getConfiguration().locale;
            language = "en";
            if (locale.getLanguage().equals("yue")) {
                language = "hant";
            } else if (locale.getLanguage().equals("zh")) {
                if (locale.getCountry().equals("HK") || locale.getCountry().equals("TW")) {
                    language = "hant";
                } else {
                    language = "cn";
                }
            }
        }
        return language;
    }

    public static boolean isCurrentLanguageEnglish(Context context) {
        return "en".equals(getLanguage(context));
    }

    public static Locale getLanguageLocaleInApp(Context context) {
        String language = getLanguage(context);
        if (language.equals("cn")) {
            return new Locale("zh", "CN");
        } else if (language.equals("hant")) {
            return new Locale("zh", "TW");
        } else {
            return new Locale("en", "US");
        }
    }

    public static void setLanguage(Context context, String language) {
        saveSharedPreferences(context, "APP_LANGUAGE", language);
    }

